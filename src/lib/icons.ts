export function iconEmoji(name: string): string {
  if (iconMap[name]) return iconMap[name];
  // User ideas store the emoji itself; icon *names* are kebab-case ascii.
  return /^[a-z0-9-]*$/.test(name) ? '🌟' : name;
}
